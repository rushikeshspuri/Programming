#include<stdio.h>

///////////////////////////////////////////////////////
//
//  Macro definition
//
///////////////////////////////////////////////////////
#define PI 3.14
 
///////////////////////////////////////////////////////
//
//  Function name : CircleArea
//  Input :         Float
//  Output :        Double
//  Description :   Calculate area of circle
//  Date :          29/05/2026
//  Author :        Rushikesh Puri
//
///////////////////////////////////////////////////////
double CircleArea(float fRadius)
{
    double dArea = 0.0;
    
    dArea = PI * fRadius * fRadius;     // PI * radius square

    return dArea;
}

///////////////////////////////////////////////////////
//
//  Application to calculate area of circle
//
///////////////////////////////////////////////////////
int main ()
{
    float fValue = 0.0f;
    double dRet = 0.0;
    
    printf("Enter Radius\n");
    scanf("%f",&fValue);

    dRet = CircleArea(fValue);

    printf("Area of Circle is : %lf", dRet);
    
    return 0;
}

