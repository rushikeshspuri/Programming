void DisplayNoLine(int iNo)
{
    int iCnt = 0;

    for(iCnt = -iNo; iCnt <= iNo; iCnt++)
    {
        printf("%d\t",iCnt);

    }

}
///////////////////////////////////////////////////////
//
//  Application to print number line
//
///////////////////////////////////////////////////////

int main ()
{
    int iValue = 0;

    printf("Enter number\n");
    scanf("%d",&iValue);

    DisplayNoLine(iValue);

    return 0;
}
