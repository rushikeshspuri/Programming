void DispalyTable(int iNo)
{
    int iCnt = 0;
    

    for (iCnt = 1; iCnt <= 10; iCnt++)
    {
        printf("%d\t", iNo * iCnt);
    }
}
///////////////////////////////////////////////////////////
//
//  Application to perform Display Table of given Number
//
///////////////////////////////////////////////////////////

int main ()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d",&iValue);

    DispalyTable(iValue);

    return 0;
}
