void Display(int iNo)
{
    if (iNo < 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
}
//////////////////////////////////////////////////////////////////////////////
//
//  Application to print hello is number is less than 10 otherwise print demo
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter your number\n");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}
