void DisplayPattern(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("$\t""*\t");

    }

}

///////////////////////////////////////////////////////
//
//  Application to accept no from user and print $  *
//
///////////////////////////////////////////////////////

int main ()
{
    int iValue = 0;

    printf("Enter number\n");
    scanf("%d",&iValue);

    DisplayPattern(iValue);

    return 0;
}
