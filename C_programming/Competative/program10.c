#include <stdio.h>

#define True 1
#define False 0

typedef int BOOL; 

//////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : CheckEven
//  Input   : Integer
//  Output  : True , False
//  Description : Accept number from user and check it is even or odd
//  Date : 24/05/2026
//  Author : Rushikesh Puri
//
//////////////////////////////////////////////////////////////////////////////////////////////
BOOL CheckEven(int iNo)
{
    if (iNo % 2 == 0)
    {
        return True;
    }
    else
    {
        return False;
    }
}
///////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to Accept number from user and check it is even or odd
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    BOOL bRet = False;

    printf("Enter your number\n");
    scanf("%d", &iValue);

    bRet = CheckEven(iValue);

    if (bRet == True)
    {
        printf("Number is Even");
    }
    else
    {
        printf("Number is Odd");
    }
}
