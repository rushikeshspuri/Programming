void PrintEven(int iNo)
{
    if(iNo <= 0)
    {
        printf("Invalid Input");
        return ;
    }

    int iCnt = 1;
    while (iCnt <= iNo)
    {
        printf("%d\t", 2 * iCnt);
        iCnt++;
    }

}
///////////////////////////////////////////////////////
//
//  Application to print Even Number
//
///////////////////////////////////////////////////////

int main ()
{
    int iValue = 0;
    
    printf("Enter your number\n");
    scanf("%d",&iValue);

    PrintEven(iValue);

    return 0;
}
