#include<stdio.h>
///////////////////////////////////////////////////////
//
//  Function name : DisplayOdd
//  Input :         Integer
//  Output :        Void 
//  Description :   to Display Odd Numbers
//  Date :          29
//  Author :        Rushikesh Puri
//
///////////////////////////////////////////////////////
void DisplayOdd(int iNo)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= iNo; iCnt += 2)
    {
        printf("%d",iCnt);
    }
}

///////////////////////////////////////////////////////
//
//  Application to to Display Odd Numbers
//
///////////////////////////////////////////////////////
int main ()
{
    int iValue = 0;

    printf("Enter number\n");
    scanf("%d",&iValue);

    DisplayOdd(iValue);

    return 0;
}

