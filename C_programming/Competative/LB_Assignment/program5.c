#include <stdio.h>

////////////////////////////////////////////////////////////
//
//  Function name : Accept
//  Input   : Integer
//  Output  : None
//  Description : accept integer from user and print *
//  Date : 24/05/2026
//  Author : Rushikesh Puri
//
////////////////////////////////////////////////////////////
void Accept(int iNo)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*");
    }
}
///////////////////////////////////////////////////////////////
//
//  Application to accept integer and print *
//
///////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter your number\n");
    scanf("%d", &iValue);

    Accept(iValue);
    return 0;
}
