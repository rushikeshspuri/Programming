///////////////////////////////////////////////////////
#include <stdio.h>

///////////////////////////////////////////////////////
//
//  Macro definition
//
///////////////////////////////////////////////////////
#define Dollar_Rate 70

///////////////////////////////////////////////////////
//
//  Function name : DollarToINR
//  Input :         Integer
//  Output :        Integer
//  Description :   Convert Dollar value into INR
//  Date :          29/05/2026
//  Author :        Rushikesh Puri
//
///////////////////////////////////////////////////////
int DollarToINR(int iNo)
{
    int iDollar = iNo * Dollar_Rate;
    return iDollar;
}

///////////////////////////////////////////////////////
//
//  Application to convert Dollar into INR
//
///////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    iRet = DollarToINR(iValue);

    printf("Value in INR is %d", iRet);

    return 0;
}

