#include <stdio.h>

////////////////////////////////////////////////////////////
//
//  Function name : Display
//  Input   : Integer
//  Output  : None
//  Description :   Application to accept integer and print *
//  Date : 24/05/2026
//  Author : Rushikesh Puri
//
/////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;

    while (iNo > iCnt)
    {
        printf("*");
        iNo--;
    }
}
///////////////////////////////////////////////////////////////
//
//  Application to accept integer and print *
//
///////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter your Number\n");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}
