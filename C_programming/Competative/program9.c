void Display(int iNo, int iFrequencey)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= iFrequencey; iCnt++)
    {
        printf("%d", iNo);
    }
}
///////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application to accept two numbers and print first number in second number of time
//
//////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iCount = 0;

    printf("Enter your Number\n");
    scanf("%d", &iValue);

    printf("Enter your Frequency\n");
    scanf("%d", &iCount);

    Display(iValue, iCount);

    return 0;
}
